#Logical operator 
print("Logical Operator Example")
a=False
b=True
#And is use to check if both conditions are true
print("a and b is", a and b)
#Or is used to check if at least one condition is true
print("a or b is", a or b)
#Not is used to invert the boolean value
print("not a is", not a)
print("not a is", not b)